#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t			len;
	size_t			i;
	unsigned char	*str;

	i = 0;
	if (!s)
		return (0);
	len = ft_strlen(s);
	str = (unsigned char *)malloc(sizeof(unsigned char) * (len + 1));
	if (!str)
		return (0);
	while (i < len)
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return ((char *)str);
}
