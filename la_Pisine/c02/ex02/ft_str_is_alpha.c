int	ft_str_is_alpha(char *str)
{
	if (str[0] == '\0')
		return (1);
	else
	{
		while (*str)
		{
			if (*str < 'A' || *str > 'Z')
				if (*str < 'a' || *str > 'z')
					return (0);
			str++;
		}
		return (1);
	}
}
